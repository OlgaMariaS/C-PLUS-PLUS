 // Programa de adição que exibe a soma de dois números.
 #include <iostream>

 using namespace std;

 int main()
 {
    int numero1;
    int numero2;
    int soma;

    cout << "Coloque o primeiro numero:";
    cin >> numero1; // armazena na variavel

    cout << "Coloque o segundo numero:";
    cin >> numero2;//armazena na variavel

    soma = numero1 + numero2; // adiciona os números; armazena o resultado em soma

    cout << "A soma dos dois e :" << soma << endl; // termina e uma outra linha

 return 0;
 }
